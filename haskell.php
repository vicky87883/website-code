<h3>Sample Program:</h3>
<h6> Here's a simple program in Haskell that calculates the factorial of a number:</h6>
<br>
<div style=background:#dcdcdc;border-radius:10px;padding:10px>
<br>
<pre>
-- Define a recursive function to calculate the factorial
factorial :: Integer -> Integer
factorial 0 = 1
factorial n = n * factorial (n - 1)

-- Main function to read input and display the factorial
main :: IO ()
main = do
  putStrLn "Enter a number:"
  numStr <- getLine
  let num = read numStr :: Integer
  let result = factorial num
  putStrLn ("The factorial of " ++ show num ++ " is " ++ show result)

	</pre>
</div>
<p>
In this program, we define a function factorial that calculates the factorial of a given number using recursion. The base case is when the input is 0, in which case the factorial is defined as 1. For any other positive number n, the factorial is calculated as n * factorial (n - 1).<br><br>
The main function is the entry point of the program. It prompts the user to enter a number, reads the input from the console, converts it to an Integer, and calculates the factorial using the factorial function. Finally, it displays the result to the user.
<br><br>
To run this program, save it in a file with a .hs extension (e.g., factorial.hs). Then, compile and run it using a Haskell compiler such as GHC (Glasgow Haskell Compiler):
</p>
<br>
<div style=background:#dcdcdc;border-radius:10px;padding:10px>
<pre>
$ ghc factorial.hs
$ ./factorial
	</pre>
</div>
<br>
<p>
Upon execution, the program will ask for a number, calculate its factorial, and display the result.
</p>
