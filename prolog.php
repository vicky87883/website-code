<h3>Sample Program:</h3>
<h6>  Here's a unique example of a Prolog program that performs a simple spell-checking task by suggesting corrections for misspelled words:</h6>
<br>
<div style=background:#dcdcdc;border-radius:10px;padding:10px>
<br>
<pre>
% Vocabulary - List of valid words
vocabulary([apple, banana, cherry, date, elderberry, fig, grape, honeydew]).

% Rule to check if a word is valid
valid(Word) :- vocabulary(V), member(Word, V).

% Rule to suggest corrections for misspelled words
suggestCorrections(Word, Corrections) :- findall(C, validCorrection(Word, C), Corrections).

% Rule to find valid corrections for a misspelled word
validCorrection(Word, Correction) :- valid(Correction), levenshteinDistance(Word, Correction, D), D < 3.

% Rule to compute the Levenshtein distance between two words
levenshteinDistance(Word1, Word2, Distance) :-
    atom_chars(Word1, Chars1),
    atom_chars(Word2, Chars2),
    length(Chars1, M),
    length(Chars2, N),
    distanceMatrix(Chars1, Chars2, M, N, Distance).

% Rule to compute the distance matrix for Levenshtein distance calculation
distanceMatrix(Chars1, Chars2, M, N, Distance) :-
    length(Matrix, M),
    initializeMatrix(Matrix, N),
    computeDistances(Chars1, Chars2, Matrix),
    last(Matrix, LastRow),
    last(LastRow, Distance).

% Rule to initialize the distance matrix
initializeMatrix([Row | RestRows], N) :-
    length(Row, N),
    numlist(0, N, Row),
    initializeMatrix(RestRows, N).
initializeMatrix([], _).

% Rule to compute the distances for each cell in the matrix
computeDistances([X | Xs], Y, [PrevRow | RestRows]) :-
    computeRowDistances(X, Y, PrevRow, CurrRow),
    computeDistances(Xs, Y, RestRows).
computeDistances([], _, _).
computeDistances(_, [], _).

% Rule to compute the distances for a row in the matrix
computeRowDistances(X, [Y | Ys], [Left | PrevRow], [Curr | CurrRow]) :-
    minimum([Left + 1, CurrRow, PrevRow], Curr),
    computeRowDistances(X, Ys, [Curr | PrevRow], CurrRow).
computeRowDistances(_, [], _, _).
computeRowDistances(_, _, [], []).

% Rule to find the minimum of a list of numbers
minimum([X | Xs], Min) :- minimum(Xs, X, Min).
minimum([], Min, Min).
minimum([X | Xs], MinSoFar, Min) :- X < MinSoFar, minimum(Xs, X, Min).
minimum([X | Xs], MinSoFar, Min) :- X >= MinSoFar, minimum(Xs, MinSoFar, Min).


