fun main() {
    val n = 10 // Change this value to calculate a different Fibonacci number
    val fib = mutableMapOf<Int, Long>() // Memoization cache

    fun fibonacci(num: Int): Long {
        if (num <= 1) return num.toLong()
        
        // Check if the result is already cached
        if (fib.containsKey(num)) return fib[num]!!

        // Calculate and cache the Fibonacci number
        val result = fibonacci(num - 1) + fibonacci(num - 2)
        fib[num] = result

        return result
    }

    val nthFibonacci = fibonacci(n)
    println("The $n-th Fibonacci number is: $nthFibonacci")
}

