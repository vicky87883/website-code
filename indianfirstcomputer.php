<h3>The Birth of CDAC: A Catalyst for Change </h3>
<p>
In 1988, Dr. Bhatkar co-founded the Centre for Development of Advanced Computing (CDAC). This institution played a pivotal role in shaping the course of India's computer industry. CDAC's primary objective was to develop indigenous supercomputers and high-performance computing systems.
</p>
<br>
<h3>The Param Series: From Vision to Reality </h3>
<p>
Under Dr. Bhatkar's leadership, CDAC embarked on an ambitious mission to create India's first indigenous supercomputer series, named Param. The journey began with Param 8000, which marked a significant breakthrough for the Indian computing ecosystem. This success spurred the subsequent development of Param 10000, Param 11000, and Param 12000, each showcasing significant advancements in computing power.
</p>
