#include <iostream>

// Function to calculate the Fibonacci sequence recursively
int fibonacci(int n) {
    if (n <= 1)
        return n;
    else
        return fibonacci(n - 1) + fibonacci(n - 2);
}

// Function to print the Fibonacci sequence up to a given number
void printFibonacciSequence(int limit) {
    for (int i = 0; fibonacci(i) <= limit; ++i) {
        std::cout << fibonacci(i) << " ";
    }
    std::cout << std::endl;
}

int main() {
    int limit;
    std::cout << "Enter the limit to generate Fibonacci sequence: ";
    std::cin >> limit;

    std::cout << "Fibonacci sequence up to " << limit << ":\n";
    printFibonacciSequence(limit);

    return 0;
}
