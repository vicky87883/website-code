<h3>
Tests of Divisibility:
</h3>
<br>
<p><b>Let us test the divisibility of various numerals in mathematics.</b></p>
	<hr class="style-two">
	
<p><b>➱ Divisibility By 2:</b><br>
A number is divisible by 2 if its unit digit is any of 0,2,4,6,8.<br>
<b>Ex.</b> 58694 is divisible by 2, while 86945 is not divisible by 2.
</p>
	<hr class="style-two">
<br>
<p><b>➱ Divisibility By 3:</b><br>
A number is divisible by 3 only when the sum of its digits is divisible by 3<br>
<b>Ex.</b>(I) In the number 695421, the sum of digits = 27, which is divisible by 3. Therefore 695421 is divisible by 3.<br>
(II) In the number 948653, the sum of digits = 35, which is not divisible by 33. Therefore 948653 is not divisible by 3.
</p>
	<hr class="style-two">
	
<br>
<p><b>➱ Divisibility By 9:</b><br>
A number is divisible by 9 only when the sum of its digits is divisible by 9<br>
<b>Ex.</b>(I) In the number 246591, the sum of digits = 27, which is divisible by 9. Therefore 246591 is divisible by 9.<br>
(II) In the number 734519, the sum of digits = 29, which is not divisible by 9. Therefore 734519 is not divisible by 9.
</p>
	<hr class="style-two">
	
<br>
<p><b>➱ Divisibility By 4:</b><br>
A number is divisible by 4 only if the number formed by its last two digits is divisible by 4.<br>
<b>Ex.</b>(I) 6879376 is divisible by 4, since 76 is divisible by 4.<br>
(II) 496138 is not divisible by 4, since 38 is not divisible by 4.
</p>
	<hr class="style-two">
	
<br>
<p><b>➱ Divisibility By 8:</b><br>
