<br>
<p><b>➱ Divisibility By 5:</b><br>
A number is divisible by 5 only when its unit digit is 0 or 5.
<br>
<b>Ex.</b>(I) Each of the numbers 76895 and 68790 is divisible by 5.
</p>
	<hr class="style-two">
<br>
<p><b>➱ Divisibility By 11:</b><br>
A number is divisible by 11 only if the difference between the sum of its digits at odd places and the sum of its digits at even places is either 0 or a number divisible by 11.
<br>
<b>Ex.</b>(I) Consider the number 29435417<br>
(Sum of its digits at odd places) - (Sum of its digits at even places)<br>
= (7 + 4 + 3 + 9) - (1 + 5 + 4 + 2) = (23-12) = 11, which is divisible by 11.<br>
∴ 29435417 is divisible by 11.
<br>
(II) Consider the number 57463822.<br>
(Sum of its digits at odd places) - (Sum of its digits at even places)<br>
= (2 + 8 + 6 + 7) - (2 + 3 + 4 + 5) = (23 - 14) = 9, which is not divisible by 11.<br>
∴ 57463822 is not divisible by 11.
</p>
	<hr class="style-two">
<br>
<p><b>➱ Divisibility By 25:</b><br>
A number is divisible by 25 if the number formed by its last two digits is either 00 or divisible by 25
<br>
<b>Ex.</b>(I) In the number 63875, the number formed by last 2 digits, namely 75 is divisible by 25.<br>
∴ 63875 is divisible by 25.<br>
(II) In the number 96445, the number formed by last 2 digit, namely 45 is not divisible by 25.<br>
∴ 96445 is not divisible by 25.<br>
</p>
	<hr class="style-two">
<br>
<p><b>➱ Divisibility By 7 or 13:</b><br>
Divide the number into groups of 3 digits (starting from right) and find the difference<br>
between the sum of the numbers in odd and even places. If the difference is 0 or divisible<br>
by 7 or 13 (as the case may be), it is divisible by 7 or 13.
<br>
<b>Ex.</b>(I) 4537792 ➜ 4 / 537 / 792.<br>
(792 + 4) - 537 = 259, which is divisible by 7 but not by 13.<br>
∴ 4537792 is divisible by 7 and not by 13.<br>
(II) 579488 ➜ 579 / 488.<br>
579 - 488 = 91, divisible by both 7 and 13.<br>
∴ 579488 is divisible by both 7 and 13.
</p>
	<hr class="style-two">
