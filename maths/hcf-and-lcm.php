<h3>
H.C.F and L.C.M of Numbers
</h3>
<br>
<p><b>Let us discuss what is hcf and lcm.</b></p>
	<br>
	<p>
	<b>➱ Factors and Multiples:</b>
	If a number a divides another number b exactly, we say that 'a' is a factor of b.
	In this case, b is called a <b>multiple of a.</b>
</p>
	<hr class="style-two">
	
<h4>➱ Highest Common Factor(H.C.F.):</h4><br>
	<p><b>or Greatest Common Measure(G.C.M) or Greatest Common Divisior (G.C.D)</b>
The H.C.F of two or more than two numbers is the greatest number that divides each of them exactly.<br>
		There are two methods of finding the H.C.F of a given set of numbers:<br><br>
		<b>1. Factorization Method</b>: Express each one of the given numbers as the product of prime factors. The Product of least powers of common prime factors gives H.C.F.</p><br>
	<p><b>2. Division Method:</b>
	Suppose we have to find the H.C.F of two given numbers. Divide the larger number by the smaller one. Now, divide the divisor by the reminder. Repeat the process of dividing the preceding number by the remainder last obtained till zero is obtained as remainder. The last divisor is the required H.C.F.<br>
		<b>Finding the H.C.F of more than two numbers:</b>
		suppose we have to find the H.C.F. of three numbers. Then, H.C.F. of (H.C.F. of any two) and (the third number) gives the H.C.F of three given numbers. similarly, the H.C.F. of more than three numbers may be obtained.
	</p>
	<hr class="style-two">
<br>
<h4>➱ Least Common Multiple (L.C.M):</h4>
	<br>
	<p>
	<b>L.C.M stand for Least Common Multiple between numerals</b>
The least number which is exactly divisible by each one of the given numbers is called their L.C.M.<br><br>
	<b>1. Factorization Method of Finding L.C.M:</b>
		Resolve each one of the given numbers into a product of prime factors. Then, L.C.M. is the product of highest powers of all the factors.<br><br>
		<b>2. Common Division Method (Short-cut Method) of Finding L.C.M:</b><br>
		Arrange the given numbers in a row in any order. Divide by a number which divides exactly at least two of the given numbers and carry forward the numbers which are not divisible. repeat the above process till no two of the numbers are divisibl eb the same number except 1. The product of the divisors and the undivided numbers is the required L.C.M. of the given numbers.
</p>
	<hr class="style-two">
<br>
<h5>➱ Product of two numbers = Product of their H.C.F and L.C.M.</h5>

	<hr class="style-two">
	<h4>➱ Coprimes: </h4>
	<p><b>Two numbers are said to be co-primes if their H.C.F is 1.</b></p>
<br>
	<hr class="style-two">
<br>
<h4>➱ H.C.F. and L.C.M. of Fractions:</h4><br>
	<div class="list-bg">
		<ul>
			<b>1. H.C.F. = (H.C.F. of Numerators) / (L.C.M. of Denominators)</b><br><br>
			<b>2. L.C.M. = (L.C.M. of Numerators) / (H.C.F. of Denominators)</b>
		</ul>
	</div>
	<hr class="style-two">
	
<br>
<h4>➱ H.C.F. and L.C.M. of Decimal Fractions:</h4>
	<p>
		In given numbers, make the same number of decimal places by annexing zeros in some numbers, if necessary. Considering these numbers without decimal point, find H.C.F. or L.C.M. as the case may be. Now, in the result, mark off as many decimal places as are there in each of the given numbers.
</p>
	<hr class="style-two">
<br>
<h4>➱ Comparison of Fractions:</h4>
	<p>Find the L.C.M. of the denominators of the given fractions. Convert rach of the fractions into an equivalent fraction with L.C.M. as the denominator, by multiplying both the numerator and denominator by the same number. The resultant fraction with the greatest numerator is the greatest.</p>
