<h3>Properties in Mathematics :</h3><br>
<p><b>➯ Commutative Property (Addition and Multiplication)</b>: Changing the order of numbers being added or multiplied doesn't change the result. For example, 2 + 3 is the same as 3 + 2, and 4 × 5 is the same as 5 × 4.</p><br>
<p><b>➯ Associative Property (Addition and Multiplication)</b>: Changing the grouping of numbers being added or multiplied doesn't change the result. For example, (1 + 2) + 3 is the same as 1 + (2 + 3), and (2 × 3) × 4 is the same as 2 × (3 × 4).</p><br>
<p><b>➯ Distributive Property</b>: Multiplying a number by a sum is the same as multiplying the number by each term in the sum and then adding the results. For example, 2 × (3 + 4) is the same as (2 × 3) + (2 × 4).</p><br>
<p><b>➯ Identity Property (Addition and Multiplication)</b>: The sum of any number and zero is the number itself, and the product of any number and one is the number itself. For example, 5 + 0 is 5, and 3 × 1 is 3.
</p><br>
<p><b>➯ Inverse Property (Addition and Multiplication)</b>: The sum of any number and its additive inverse (opposite) is zero, and the product of any number and its multiplicative inverse (reciprocal) is one. For example, 7 + (-7) is 0, and 2 × (1/2) is 1.</p><br>
<p><b>➯ Zero Property of Multiplication</b>: Any number multiplied by zero is zero. For example, 5 × 0 is 0.</p><br>
<p><b>➯ Exponentiation Property</b>: When a number is raised to a power (exponent), it represents repeated multiplication. For example, 2^3 means 2 × 2 × 2, which is 8.</p><br>
<p><b>➯ Order of Operations (PEMDAS/BODMAS)</b>: The sequence in which mathematical operations should be performed to get the correct result: Parentheses/Brackets, Exponents/Orders, Multiplication and Division (from left to right), and Addition and Subtraction (from left to right).</p><br>
<p><b>➯ Symmetry</b>: A shape or object is symmetrical if one side is a mirror image of the other side. Symmetry is often found in geometric figures like circles and squares.</p><br>
<p><b>➯ Transitivity (Inequality)</b>: If one number is greater than another, and the second number is greater than a third number, then the first number is also greater than the third number. For example, if 5 > 3 and 3 > 1, then 5 > 1.</p><br>
<p>These simple properties help us understand and manipulate numbers, operations, and geometric shapes, making <a href=#>mathematics</a> easier to work with and apply in various situations.</p>
