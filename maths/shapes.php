<h3>
	Shapes
</h3>
<br>
<p><b>Let us discuss what is shapes in mathematics.</b></p>
<p>
	In  <a href="https://www.codeavenues.com/maths/what-is-maths">mathematics</a>, shapes refer to different <a>geometric</a> figures or forms that can be identified by their specific attributes such as size, angles, and sides. Shapes can be classified into two main categories: <b>2D</b> shapes, which are flat and have only length and width <b>(e.g., circles, triangles, squares)</b>, and <b>3D</b> shapes, which are solid and have length, width, and height <b>(e.g., cubes, spheres, pyramids)</b>. Understanding shapes is essential in <a>geometry</a>, as it helps us analyze and describe the world around us, from simple everyday objects to complex structures.
</p>
<br>
<hr class=style-two>
<h4>➱ What are Shapes:</h4><br>
<p>
	In pure <a href="https://www.codeavenues.com/maths/what-is-maths"> mathematics</a>, shapes are fundamental objects defined in <a>geometry</a>. A shape is a geometric figure that has specific properties and characteristics, such as size, <b>dimensions</b>, <b>angles</b>, and <b>symmetry</b>. Shapes are considered abstract entities that are studied for their intrinsic properties rather than their physical manifestations.</p>
<br>
<p>Shapes can be classified into different categories based on their dimensions and properties. Two main categories are:</p>
<br>
<p>	<b>1. Two-dimensional (2D) Shapes</b>: These shapes exist in a flat plane and have only length and width. Examples include circles, triangles, squares, rectangles, and polygons.</p>
<br>
	<p><b>2. Three-dimensional (3D) Shapes</b>: These shapes have length, width, and height and exist in a <b>three-dimensional</b> space. Examples include cubes, spheres, pyramids, cylinders, and cones.</p>
<br>
	<p>In pure <a>mathematics</a>, shapes are studied rigorously using axioms, definitions, and theorems in <a>geometry</a>. This discipline focuses on understanding the properties, relationships, and transformations of shapes, forming the basis for various <a>mathematical</a> analyses and developments.</p>
<hr class=style-two>
<br>
<h4>➱ Types of Shapes:</h4><br>
	<p>There are various types of shapes in mathematics, classified based on their dimensions and properties. Here's a detailed overview of some common types of shapes:</p>
	<p><b>List of Two Dimension Shapes:</b></p>
	<div class="list-bg">
		<ul>
			<p>Two-Dimensional (2D) Shapes:</p>
			<p>a. Point: A single location in space, represented by a dot.</p>
			<p>b. Line: A straight path that extends infinitely in both directions.</p>
			<p>c. Ray: A part of a line with one endpoint and extending infinitely in one direction.</p>
			<p>d. Line Segment: A part of a line with two endpoints, having a definite length.</p>
			<p>e. Angle: Formed by two rays sharing a common endpoint (vertex).</p>
			<p>f. Triangle: A 2D shape with three sides and three angles.</p>
			<p>g. Quadrilateral: A 2D shape with four sides and four angles.</p>
			<p>h. Circle: A 2D shape with all points equidistant from the center.</p>
			<p>i. Polygon: A closed 2D shape with straight sides and angles.</p>
		</ul>
	</div>
	<br>
	<p><b>List of Three Dimension Shapes:</b></p>
	<div class="list-bg">
		<ul>
			<p>Three-Dimensional (3D) Shapes:</p>
			<p>a. Cube: A 3D shape with six square faces of equal size.</p>
			<p>b. Sphere: A 3D shape with all points equidistant from the center, like a ball.</p>
			<p>c. Cylinder: A 3D shape with two circular bases and a curved lateral surface.</p>
			<p>d. Cone: A 3D shape with a circular base and a single vertex.</p>
			<p>e. Pyramid: A 3D shape with a polygonal base and triangular faces converging at a vertex.</p>
			<p>f. Cuboid/Rectangular Prism: A 3D shape with six rectangular faces.</p>
			<p>g. Tetrahedron: A 3D shape with four triangular faces.</p>

		</ul>
	</div>
	<p>These are some of the fundamental types of shapes studied in mathematics. They serve as building blocks for various <b>geometrical</b> concepts and play a crucial role in diverse areas like architecture, engineering, art, and computer graphics.</p>
<br>
<hr class=style-two>
<br>
<h5>➱ Polygon in Shapes</h5>
	<p>
		A <b>polygon</b> is a closed <b>two-dimensional</b> shape with straight sides. It is formed by connecting line segments (sides) to create a closed path. The angles between the sides are called vertices. <b>Polygons</b> have no curves or openings, and they lie entirely in one plane.

		In simple words, think of a <b>polygon</b> as a shape that has straight sides and looks like a <b>"flat"</b> figure. It can be any shape as long as it meets the criteria of having straight sides and forming a closed shape. Some common examples of <b>polygons</b> include <b>triangles (3 sides)</b>, <b>squares (4 sides)</b>, <b>pentagons (5 sides)</b>, <b>hexagons (6 sides)</b>, and <b>octagons (8 sides)</b>. The more sides a polygon has, the more complex its shape becomes.
	</p>
<br>
<p><b>➱ Types of Polygon:</b></p>
	<p> Polygons are classified based on the number of sides they have and their unique characteristics. Here are some common polygons in detail:</p>
<div class=list-bg>
<ul>
<p>
	<b>1. Triangle</b>:</p>
<p>Three sides and three angles.<br>
	<b>Types</b>: Equilateral (all sides and angles are equal), Isosceles (two sides and two angles are equal), Scalene (all sides and angles are different), and Right-angled (has one 90-degree angle).</p>
	<br>
	<p><b>2. Quadrilateral</b>:</p>
	
<p>Four sides and four angles.<br>
	<b>Types</b>: Square (all sides and angles are equal, right angles), Rectangle (opposite sides are equal and parallel, right angles), Rhombus (all sides are equal, opposite angles are equal), Parallelogram (opposite sides are equal and parallel), and Trapezium (only one pair of sides are parallel).</p>
	<br>
	<p><b>3. Pentagon</b>:</p>

	<p>Five sides and five angles.</p>
	<br>
	<p><b>4. Hexagon</b>:</p>
	<p>Six sides and six angles.</p>
	<br>
	<p><b>5. Heptagon (or Septagon)</b>:</p>
	<p>Seven sides and seven angles.</p>
	<br>
	<p><b>6. Octagon</b>:</p>
	<p>Eight sides and eight angles.</p>
	<br>
	<p><b>7. Nonagon (or Enneagon)</b>:</p>
	<p>Nine sides and nine angles.</p>
	<br>
	<p><b>8. Decagon</b>:</p>
	<p>Ten sides and ten angles.</p>
	<br>
	<p><b>9. Dodecagon</b>:</p>
	<p>Twelve sides and twelve angles.</p>
</ul>
</div>
